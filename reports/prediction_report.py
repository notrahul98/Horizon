"""
swing_trader/reports/prediction_report.py
Generate daily prediction reports
"""

import logging
from datetime import datetime

logger = logging.getLogger(__name__)

class PredictionReport:
    """Generate trading prediction reports"""
    
    def __init__(self):
        self.predictions = []
    
    def add_prediction(self, stock_analysis, consensus):
        """Add a prediction to the report"""
        
        if not stock_analysis or not consensus:
            return
        
        prediction_entry = {
            'symbol': stock_analysis.get('symbol', 'UNKNOWN'),
            'date': datetime.now().isoformat(),
            'price': stock_analysis.get('price', 0),
            'signal': consensus.get('consensus_signal', 'HOLD'),
            'confidence': consensus.get('confidence', 0),
            'conviction': consensus.get('conviction', 'LOW'),
            'stop_loss': stock_analysis.get('stop_loss', 0),
            'target_1': stock_analysis.get('target_1', 0),
            'target_2': stock_analysis.get('target_2', 0),
            'agent_votes': consensus.get('votes', {}),
        }
        
        self.predictions.append(prediction_entry)
    
    def generate_summary(self):
        """Generate summary report"""
        
        if not self.predictions:
            return {
                'date': datetime.now().isoformat(),
                'total_predictions': 0,
                'buy_signals': 0,
                'sell_signals': 0,
                'hold_signals': 0,
                'strong_signals': 0,
                'predictions': []
            }
        
        buy_count = sum(1 for p in self.predictions if p['signal'] == 'BUY')
        sell_count = sum(1 for p in self.predictions if p['signal'] == 'SELL')
        hold_count = sum(1 for p in self.predictions if p['signal'] == 'HOLD')
        strong_count = sum(1 for p in self.predictions 
                          if p['conviction'] in ['HIGH', 'MEDIUM'])
        
        return {
            'report_date': datetime.now().isoformat(),
            'total_predictions': len(self.predictions),
            'buy_signals': buy_count,
            'sell_signals': sell_count,
            'hold_signals': hold_count,
            'strong_signals': strong_count,
            'predictions': sorted(
                self.predictions,
                key=lambda x: x['confidence'],
                reverse=True
            )
        }
    
    def format_text(self):
        """Format report as readable text"""
        
        summary = self.generate_summary()
        
        text = f"""
╔══════════════════════════════════════════════════════════════════╗
║           🎯 DAILY TRADING PREDICTIONS REPORT                   ║
╚══════════════════════════════════════════════════════════════════╝

📅 Report Date: {summary['report_date']}

📊 SUMMARY
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  Total Predictions: {summary['total_predictions']}
  🟢 BUY Signals:    {summary['buy_signals']}
  🔴 SELL Signals:   {summary['sell_signals']}
  ⚪ HOLD Signals:   {summary['hold_signals']}
  ⭐ Strong Signals: {summary['strong_signals']}

🏆 TOP PREDICTIONS (by confidence)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
"""
        
        for i, pred in enumerate(summary['predictions'][:10], 1):
            signal_emoji = '🟢' if pred['signal'] == 'BUY' else '🔴' if pred['signal'] == 'SELL' else '⚪'
            
            text += f"""
{i}. {signal_emoji} {pred['symbol']}
   Signal: {pred['signal']} | Confidence: {pred['confidence']}% | {pred['conviction']}
   Entry: {pred['price']:.2f} | SL: {pred['stop_loss']:.2f} | T1: {pred['target_1']:.2f} | T2: {pred['target_2']:.2f}
   Votes: 🟢{pred['agent_votes'].get('buy', 0)} 🔴{pred['agent_votes'].get('sell', 0)} ⚪{pred['agent_votes'].get('hold', 0)}
"""
        
        text += """
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ Report generated by Phase 1 Trading System
"""
        
        return text
    
    def clear(self):
        """Clear predictions for new report"""
        self.predictions = []
